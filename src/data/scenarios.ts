const createScenario = (
  id: string,
  title: string,
  shortTitle: string,
  description: string,
  intro: string,
  whenToUse: string[],
  riskLevel: Scenario['riskLevel'],
  accent: string,
  tips: string[],
  steps: Step[],
  extras?: Pick<Scenario, 'questions' | 'branchRules' | 'defaultRecommendation'>,
): Scenario => ({
  id,
  title,
  shortTitle,
  description,
  intro,
  whenToUse,
  riskLevel,
  accent,
  redFlags: commonRedFlags,
  tips,
  documents: Array.from(
    new Map(
      steps
        .flatMap((step) => [step.templateFile, ...(step.relatedTemplates ?? [])])
        .filter((template): template is TemplateResource => Boolean(template))
        .map((template) => [template.id, template]),
    ).values(),
  ),
  steps,
  questions: extras?.questions,
  branchRules: extras?.branchRules,
  defaultRecommendation: extras?.defaultRecommendation,
});

